package model;

public class NilaiKompetensi {
	private Integer id;
	private String idKrt1;
	private String idKrt2;
	private Double nilai;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getIdKrt1() {
		return idKrt1;
	}
	public void setIdKrt1(String idKrt1) {
		this.idKrt1 = idKrt1;
	}
	public String getIdKrt2() {
		return idKrt2;
	}
	public void setIdKrt2(String idKrt2) {
		this.idKrt2 = idKrt2;
	}
	public Double getNilai() {
		return nilai;
	}
	public void setNilai(Double nilai) {
		this.nilai = nilai;
	}
}
